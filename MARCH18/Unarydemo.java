class Unarydemo{
	public static void main(String []args){
		int a=10;
		System.out.println("increment");
		System.out.println(++a + a);
		int a= 10;
		System.out.println(a + ++a);
		int a = 10;
		System.out.println(++a + ++a);
		int a=10;
		System.out.println(a++ + ++a);
		a=10;
		System.out.println(a++ + a++);
	}
}